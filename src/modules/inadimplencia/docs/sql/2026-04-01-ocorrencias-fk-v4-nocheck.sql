BEGIN TRY
  BEGIN TRAN;

  IF NOT EXISTS (
    SELECT 1
    FROM sys.indexes
    WHERE object_id = OBJECT_ID('dw.fat_analise_inadimplencia_v4')
      AND name = 'UQ_FAT_INAD_V4_NUM_VENDA'
  )
  BEGIN
