    SELECT 1
    FROM sys.foreign_keys
    WHERE parent_object_id = OBJECT_ID('dbo.OCORRENCIAS')
      AND name = 'FK_OCORRENCIAS_FAT_NUMVENDA_V2'
  )
  BEGIN
    ALTER TABLE dbo.OCORRENCIAS
      DROP CONSTRAINT FK_OCORRENCIAS_FAT_NUMVENDA_V2;
  END;

  IF NOT EXISTS (
    SELECT 1
    FROM sys.foreign_keys
    WHERE parent_object_id = OBJECT_ID('dbo.OCORRENCIAS')
